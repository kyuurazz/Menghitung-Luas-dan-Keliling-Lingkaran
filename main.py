print("Program Menghitung Luas dan Keliling Lingkaran")
print("==============================================")
r = float(input("Masukan Jari-Jari Lingkaran  : "))

phi = 3.14
luas = phi*r*r
kel = 2*phi*r

print ("Luas Lingkaran               :",format(luas,'.2f'))
print ("Keliling Lingkaran           :",format(kel,'.2f'))